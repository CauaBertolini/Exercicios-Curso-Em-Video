# Crie um scrip ou programa que leia dois numeros e mostre o resultado certo da soma entre eles.
n1 = int(input('\033[35mDigite um numero: '))
n2 = int(input('\033[35mDigite outro numero: '))
r = n1 + n2
print('\033[31mA soma de \033[34m{}\033[31m e \033[34m{}\033[31m resulta em \033[34m{}!'.format(n1, n2, r))
